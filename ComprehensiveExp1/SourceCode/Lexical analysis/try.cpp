void PrintTable(const vector<vector<int>>& FATable)
{
	if (FATable.size() > 200)
		return;

	int row = FATable[0][0];
	int col = FATable[0][1];
	string output = "";
	for (int i = 0; i < row; i++)
	{
		for (int j = 0; j < col; j++)
		{
			for (int id : FATable[i * col + j])
			{
				if (i == 0 && j != 0)
					output += id;
				else
					output += to_string(id) + ",";
			}
			output += "\t";
		}
		output += '\n';
	}
	cout << output;
	cout.flush();

	int i = 0;
}

