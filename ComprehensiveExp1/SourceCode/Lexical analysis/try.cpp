void TransferStatus(int& CurrentStatusID, char Condition, const vector<vector<int>>& MinDFA)
{
	int MinDFACol = MinDFA[0][1];

	// 转移状态
	int AnyCol = -1;// 若转移条件中存在任意符号指代符'.'，则记录这个转移条件所在的列
	for (int col = 1; col < MinDFACol; col++)
	{
		if (MinDFA[col][0] == Condition)
		{
			if (MinDFA[CurrentStatusID * MinDFACol + col].size() == 1)
			{
				CurrentStatusID = MinDFA[CurrentStatusID * MinDFACol + col][0];
				return;
			}
		}
		else if (MinDFA[col][0] == '.')
			AnyCol = col;
	}
		
	if (AnyCol != -1)
	{
		CurrentStatusID = MinDFA[CurrentStatusID * MinDFACol + AnyCol][0];
		return;
	}

	CurrentStatusID = MinDFA[0][2];// 如果状态无法转移，则回到起始节点
}

